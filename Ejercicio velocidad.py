#Calcula la velocidad promedio, dado el tiempo y la distancia
#A01377601

d = int(input("Dame la distancia de tu viaje en Km enteros: "))
t = int(input("Dame el tiempo de tu viaje en Horas enteras: "))

#----------------------------------------------------------------------------------------------------------------------#

v = d / t

#----------------------------------------------------------------------------------------------------------------------#

print("Tu velocidad promedio es: ", v , "km/h")